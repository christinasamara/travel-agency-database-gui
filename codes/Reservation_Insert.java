               myConn = DriverManager.getConnection("jdbc:mysql://localhost:3306/travel_agency", user, pass);     
               String sql1 = "SELECT COUNT(*) FROM reservation";
               int temp_value = 0;
               Statement st = myConn.createStatement();
               myRs = st.executeQuery(sql1);
               while(myRs.next()){
                   temp_value = myRs.getInt("COUNT(*)");
               }
               
               String sql2 = "INSERT INTO reservation(res_tr_id, res_seatnum, res_name, res_lname, res_isadult) VALUES(?,?,?,?,?)";
