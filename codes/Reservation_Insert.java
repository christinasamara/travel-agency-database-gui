    public ArrayList<reservation> userList(){
        ArrayList<reservation> usersList = new ArrayList<>();
        Connection myConn = null;
        ResultSet myRs = null;
        
        String user = "root";
        String pass = "Skazas082002";
        try{
               myConn = DriverManager.getConnection("jdbc:mysql://localhost:3306/travel_agency", user, pass);
               String sql = "SELECT * FROM reservation";
               Statement statement = myConn.createStatement();
               myRs = statement.executeQuery(sql);
               reservation res;
               while(myRs.next()){
                   res = new reservation(myRs.getInt("res_tr_id"), myRs.getInt("res_seatnum"), myRs.getString("res_name"), myRs.getString("res_lname"), myRs.getString("res_isadult"));
                   usersList.add(res);
               }
               
        }catch(SQLException exc){
               exc.printStackTrace();
        }finally{
            try{
                if(myRs != null){
                    myRs.close();
                }
                if(myConn != null){
                    myConn.close();
                }
            }catch(SQLException exc){
                   exc.printStackTrace();
            }
        }
        return usersList;
    }
    
