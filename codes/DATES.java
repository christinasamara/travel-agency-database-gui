    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:

        Connection myConn = null;
        ResultSet myRs = null;
        Statement st = null;

        String user = "root";
        String pass = "root";
        int selectedValue = branch.getSelectedIndex() + 1;
        String start = jstart.getText();
        String end = jend.getText();
        
        

        try {
            myConn = DriverManager.getConnection("jdbc:mysql://localhost:3306/travel_agency", user, pass);
            st = myConn.createStatement();
            String sql = "SELECT tr_id,  tr_cost, tr_maxseats, COUNT(*), tr_maxseats - COUNT(*), w1.wrk_lname, w1.wrk_name, w2.wrk_lname, "
                    + "w2.wrk_name, tr_departure, tr_return "
                    + "FROM trip JOIN reservation ON res_tr_id = tr_id "
                    + "JOIN worker as w1 ON w1.wrk_AT = tr_gui_AT "
                    + "JOIN worker as w2 ON w2.wrk_AT = tr_drv_AT "
                    + "WHERE tr_br_code = '" + selectedValue + "' "
                    + "AND tr_departure >= '" + start + "' "
                    + "AND tr_return <= '" + end + "' "
                    + "GROUP BY res_tr_id";
            myRs = st.executeQuery(sql);
            DefaultTableModel tb1Model = (DefaultTableModel) jTable1.getModel();
            tb1Model.setRowCount(0);

            while (myRs.next()) {
                String c1 = String.valueOf(myRs.getString("tr_id"));
                String c2 = String.valueOf(myRs.getString("tr_cost"));
                String c3 = String.valueOf(myRs.getString("tr_maxseats"));
                String c4 = String.valueOf(myRs.getString("COUNT(*)"));
                String c5 = String.valueOf(myRs.getString("tr_maxseats - COUNT(*)"));
                String c6 = String.valueOf(myRs.getString("w1.wrk_lname"));
                String c7 = String.valueOf(myRs.getString("w1.wrk_name"));
                String c8 = String.valueOf(myRs.getString("w2.wrk_lname"));
                String c9 = String.valueOf(myRs.getString("w2.wrk_name"));
                String c10 = String.valueOf(myRs.getString("tr_departure"));
                String c11 = String.valueOf(myRs.getString("tr_return"));

                String tbData[] = { c1, c2, c3, c4, c5, c6, c7, c8, c9, c10, c11 };

                tb1Model.addRow(tbData);
            }

        } // Catch bblock to handle DB exceptions
        catch (SQLException exc) {
            exc.printStackTrace();
        } finally {
            try {
                if (myRs != null) {
                    myRs.close();
                }
                if (myConn != null) {
                    System.out.println("Connection Successful");
                    myConn.close();
                }
            } catch (SQLException exc) {
                exc.printStackTrace();
            }
        }
    }//GEN-LAST:event_searchActionPerformed

